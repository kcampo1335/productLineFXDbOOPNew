package sample;

/**
 * The interface Multimedia control is where the functions for audioplayer and movieplayer are found.
 *
 * @author Kathryn Campo
 */
public interface MultimediaControl {
    /**
     * Play.
     */
    public void play();

    /**
     * Stop.
     */
    public void stop();

    /**
     * Previous.
     */
    public void previous();

    /**
     * Next.
     */
    public void next();

}